import mongoose, { Schema } from "mongoose";
import UserInterface from "../../interfaces/users.interface";

const UserSchema: Schema<UserInterface> = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default mongoose.model<UserInterface>("User", UserSchema);
